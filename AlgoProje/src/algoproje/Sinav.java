    private void bitirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bitirButtonActionPerformed
        File file=new File("ogrenciFile.txt");
        String oncekiDosya="";
        String line="";
        if (!file.exists()) {
            try {
                file.createNewFile();
            } catch (IOException ex) {
                Logger.getLogger(Sinav.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        try {
            FileReader fr1=new FileReader(file);
            BufferedReader br1=new BufferedReader(fr1);
            while ((line=br1.readLine())!=null) {                
                oncekiDosya+=line+"\n";
            }
            br1.close();
            FileWriter fw1=new FileWriter(file);
            BufferedWriter bw1=new BufferedWriter(fw1);
            //bw1.newLine();
            bw1.write(oncekiDosya);
            bw1.write(num);
            bw1.write("  "+ad);
            bw1.write("  "+not);
            bw1.close();
            
            
        } catch (IOException ex) {
            Logger.getLogger(Sinav.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_bitirButtonActionPerformed

